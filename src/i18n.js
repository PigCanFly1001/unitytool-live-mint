// 当前语言 (localStorage 记忆 → 浏览器语言 → en)
function detectLang() {
  const saved = localStorage.getItem("mr_lang");
  if (saved && DICT[saved]) return saved;
  const navs = (navigator.languages || [navigator.language || "en"]);
  for (const l of navs) {
    const code = l.toLowerCase().split("-")[0];
    if (DICT[code]) return code;
    // 中文变体归一
    if (code === "zh") return "zh";
  }
  return "en";
}

let LANG = detectLang();
const listeners = new Set();

export function getLang() { return LANG; }
export function setLang(code) {
  if (!DICT[code]) return;
  LANG = code;
  localStorage.setItem("mr_lang", code);
  document.documentElement.lang = code;
  listeners.forEach(fn => { try { fn(code); } catch {} });
}
export function onLangChange(fn) { listeners.add(fn); }

// 翻译: t("key", {n: 3})  支持 {var} 插值; 缺失回退 en → key
export function t(key, vars) {
  let s = (DICT[LANG] && DICT[LANG][key]) || DICT.en[key] || key;
  if (vars) for (const k in vars) s = s.replace(new RegExp("\\{" + k + "\\}", "g"), vars[k]);
  return s;
}

document.documentElement.lang = LANG;
